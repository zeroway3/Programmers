WITH SORTED_ECOLI AS (
    SELECT 
        ID,
        SIZE_OF_COLONY,
        ROW_NUMBER() OVER (ORDER BY SIZE_OF_COLONY DESC) AS RN,
        (SELECT COUNT(*) FROM ECOLI_DATA) AS TOTAL_COUNT
    FROM ECOLI_DATA
)

SELECT 
    ID,
    CASE 
        WHEN RN <= TOTAL_COUNT * 0.25 THEN 'CRITICAL'
        WHEN RN <= TOTAL_COUNT * 0.50 THEN 'HIGH'
        WHEN RN <= TOTAL_COUNT * 0.75 THEN 'MEDIUM'
        ELSE 'LOW'
    END AS COLONY_NAME
FROM SORTED_ECOLI
ORDER BY ID;
